package EcoMonitoring.models.CrisisDamageParams;

public class AirParams {
    public Double miEmission;
    public Double sp;
    public Double kneB;
    public Double kv;
    public Double kmp;
    public Double kpp;

    public Double getMiEmission() {
        return miEmission;
    }

    public void setMiEmission(Double miEmission) {
        this.miEmission = miEmission;
    }

    public Double getSp() {
        return sp;
    }

    public void setSp(Double sp) {
        this.sp = sp;
    }

    public Double getKneB() {
        return kneB;
    }

    public void setKneB(Double kneB) {
        this.kneB = kneB;
    }

    public Double getKv() {
        return kv;
    }

    public void setKv(Double kv) {
        this.kv = kv;
    }

    public Double getKmp() {
        return kmp;
    }

    public void setKmp(Double kmp) {
        this.kmp = kmp;
    }

    public Double getKpp() {
        return kpp;
    }

    public void setKpp(Double kpp) {
        this.kpp = kpp;
    }
}
